    public static function toBrandName(string $url): string
    {
        $host = parse_url($url, PHP_URL_HOST);

        if (! is_string($host)) {
            return 'Unknown';
        }

        foreach (static::$mapping as $brand => $domains) {
            if (Str::endsWith($host, $domains)) {
                return $brand;
            }
        }

        return 'Unknown';
    }
